def add_scale_bar(ax, location=(0.04, 0.04), length_fraction=0.25, color="black"):
    """Skala batang sederhana, dihitung otomatis ke angka 'bulat' terdekat
    (mis. 200 m / 500 m / 1 km) berdasarkan lebar peta saat ini (CRS meter)."""
    xlim, ylim = ax.get_xlim(), ax.get_ylim()
    width = xlim[1] - xlim[0]
    raw_len = width * length_fraction
    magnitude = 10 ** np.floor(np.log10(raw_len))
    nice_len = round(raw_len / magnitude) * magnitude

    x0 = xlim[0] + width * location[0]
    y0 = ylim[0] + (ylim[1] - ylim[0]) * location[1]

    ax.plot([x0, x0 + nice_len], [y0, y0], color=color, lw=4, solid_capstyle="butt", zorder=10)
    ax.plot([x0, x0], [y0 - width * 0.005, y0 + width * 0.005], color=color, lw=1.5, zorder=10)
    ax.plot([x0 + nice_len, x0 + nice_len], [y0 - width * 0.005, y0 + width * 0.005],
            color=color, lw=1.5, zorder=10)

    label = f"{nice_len / 1000:.1f} km" if nice_len >= 1000 else f"{nice_len:.0f} m"
    ax.text(x0 + nice_len / 2, y0 + width * 0.012, label, ha="center", va="bottom",
             fontsize=9, fontweight="bold", zorder=10)


def add_north_arrow(ax, location=(0.94, 0.90), size=0.045):
    """Arah mata angin (panah + huruf N) di pojok kanan-atas peta."""
    x, y = location
    ax.annotate(
        "N", xy=(x, y), xytext=(x, y - size), xycoords="axes fraction",
        textcoords="axes fraction", ha="center", va="center", fontsize=13, fontweight="bold",
        arrowprops=dict(facecolor="black", edgecolor="black", width=4, headwidth=11, headlength=10),
        zorder=10,
    )

