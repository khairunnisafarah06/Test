print("\n[5.2] ANALISIS SENSITIVITAS LANJUTAN")
print("-" * 70)

N_WEIGHT_SAMPLES = 1000
TOP_N = 20

# --- Sampling bobot acak dari distribusi Dirichlet (w1+w2+w3=1, w>=0) ---
# alpha=(4,4,4) -> bobot condong di sekitar area seimbang, tapi tetap
# memberi variasi luas mendekati kombinasi bobot ekstrem.
alpha = np.array([4.0, 4.0, 4.0])
weight_samples = np.random.dirichlet(alpha, size=N_WEIGHT_SAMPLES)

score_matrix = np.zeros((len(buildings), N_WEIGHT_SAMPLES))
rank_matrix = np.zeros((len(buildings), N_WEIGHT_SAMPLES), dtype=int)

pv = buildings["pv_index"].values
econ = buildings["econ_index"].values
env = buildings["env_index"].values

for i, (w1, w2, w3) in enumerate(weight_samples):
    s = w1 * pv + w2 * econ + w3 * env
    score_matrix[:, i] = s
    rank_matrix[:, i] = pd.Series(s).rank(ascending=False, method="min").astype(int).values

# --- Ranking utama sebagai acuan ---
main_rank = buildings["rank"].values

# --- Korelasi ranking (Spearman & Kendall) antara skenario UTAMA vs tiap sampel bobot ---
spearman_vals = [spearmanr(main_rank, rank_matrix[:, i]).correlation for i in range(N_WEIGHT_SAMPLES)]
kendall_vals = [kendalltau(main_rank, rank_matrix[:, i]).correlation for i in range(N_WEIGHT_SAMPLES)]

print(f"Simulasi {N_WEIGHT_SAMPLES} kombinasi bobot acak (Dirichlet) vs skenario utama:")
print(f"   Korelasi Spearman rata-rata : {np.mean(spearman_vals):.3f} "
      f"(min={np.min(spearman_vals):.3f}, max={np.max(spearman_vals):.3f})")
print(f"   Korelasi Kendall-tau rata-rata: {np.mean(kendall_vals):.3f} "
      f"(min={np.min(kendall_vals):.3f}, max={np.max(kendall_vals):.3f})")

# --- Perubahan ranking (delta rank) tiap bangunan akibat variasi bobot ---
rank_delta = rank_matrix - main_rank.reshape(-1, 1)
buildings["rank_delta_mean"] = np.mean(np.abs(rank_delta), axis=1)
buildings["rank_delta_std"] = np.std(rank_matrix, axis=1)

print("\nStatistik perubahan ranking (|delta rank| terhadap skenario utama), seluruh bangunan:")
print(buildings["rank_delta_mean"].describe().round(2))

# --- Identifikasi BANGUNAN PRIORITAS STABIL ---
# "Stabil" = tetap masuk Top-N di proporsi besar sampel bobot acak.
in_topN = (rank_matrix <= TOP_N)
buildings["topN_inclusion_prob"] = in_topN.mean(axis=1)  # 0-1

STABLE_THRESHOLD = 0.80  # >=80% sampel bobot -> dianggap stabil
buildings["priority_stable"] = buildings["topN_inclusion_prob"] >= STABLE_THRESHOLD

stable_buildings = buildings.loc[
    buildings["priority_stable"], ["building_id", "rank", "priority_score", "topN_inclusion_prob"]
].sort_values("topN_inclusion_prob", ascending=False)

n_stable = len(stable_buildings)
print(f"\n🏆 Bangunan PRIORITAS STABIL (masuk Top-{TOP_N} pada >= {STABLE_THRESHOLD*100:.0f}% "
      f"dari {N_WEIGHT_SAMPLES} kombinasi bobot acak): {n_stable} bangunan")
print(stable_buildings.head(20).to_string(index=False))

