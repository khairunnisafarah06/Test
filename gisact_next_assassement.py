# -----------------------------------------------------
# CELL E — Ekstrak nilai raster ke setiap bangunan (BATCH)
# -----------------------------------------------------
# PENTING: reduceRegions().getInfo() menarik SEMUA geometri + hasil sekaligus
# ke client. Untuk bangunan dalam jumlah besar ini gampang melebihi limit
# payload GEE (10 MB). Solusi: proses per-batch (mis. 200-500 bangunan/batch),
# dan getInfo() hanya properti penting (bukan geometri penuh).

# scale disesuaikan dengan resolusi terkasar di antara layer (Cloud Fraction ~1km),
# tapi GHI/DNI/GTI dari Global Solar Atlas biasanya ~250m, jadi scale=250 cukup wajar.

BATCH_SIZE = 1000

def extract_stack_to_buildings(buildings_gdf, stack_image, scale=250, batch_size=BATCH_SIZE):
    """
    Ekstrak nilai raster (mean per polygon) ke setiap bangunan secara batch,
    untuk menghindari GEE payload limit error.
    """
    n = len(buildings_gdf)
    all_rows = []

    for start in range(0, n, batch_size):
        end = min(start + batch_size, n)
        batch_gdf = buildings_gdf.iloc[start:end][["building_id", "geometry"]]

        # Convert hanya batch ini ke ee.FeatureCollection
        batch_fc = geemap.geopandas_to_ee(batch_gdf)

        sampled_batch = stack_image.reduceRegions(
            collection=batch_fc,
            reducer=ee.Reducer.mean(),
            scale=scale,
            tileScale=4,   # naikkan tileScale agar komputasi server lebih ringan per request
        )

        # Hanya ambil properti (building_id + hasil reduce), BUKAN geometri,
        # supaya payload getInfo() jauh lebih kecil.
        sampled_props = sampled_batch.select(
            propertySelectors=["building_id"] + stack_image.bandNames().getInfo(),
            retainGeometry=False,
        )

        try:
            batch_info = sampled_props.getInfo()
        except ee.EEException as e:
            print(f"   ⚠️ Batch {start}-{end} gagal ({e}), mencoba batch lebih kecil...")
            # Fallback: pecah batch ini jadi dua, proses ulang rekursif
            half = max(1, (end - start) // 2)
            sub1 = extract_stack_to_buildings(
                buildings_gdf.iloc[start:start+half], stack_image, scale, batch_size=half
            )
            sub2 = extract_stack_to_buildings(
                buildings_gdf.iloc[start+half:end], stack_image, scale, batch_size=half
            )
            all_rows.extend(sub1.to_dict("records"))
            all_rows.extend(sub2.to_dict("records"))
            continue

        for feat in batch_info["features"]:
            all_rows.append(feat["properties"])

        print(f"   Batch {start}-{end} selesai ({len(batch_info['features'])} bangunan).")

    return pd.DataFrame(all_rows)


