# 5.3  UJI ROBUSTNESS
#      (a) Monte Carlo ketidakpastian input
#      (b) Bootstrap ranking
#      (c) Out-of-sample test (model prediktif Task 2)
# ==========================================================
print("\n[5.3] UJI ROBUSTNESS")
print("-" * 70)

# -----------------------------------------------------
# (a) Monte Carlo — ketidakpastian NILAI INPUT (bukan hanya bobot)
# -----------------------------------------------------
# Mensimulasikan ketidakpastian pengukuran/derivasi indeks (misal akibat
# resolusi raster GHI/DNI/GTI ~250m, rata-rata cloud fraction 1 tahun,
# proxy ekonomi, dll) dengan menambahkan noise Gaussian kecil pada
# ketiga indeks, DIKOMBINASIKAN dengan bobot acak dari 5.2.
N_MC = 1000
NOISE_STD = 0.05  # asumsi ketidakpastian ~5% dari skala indeks (0-1)

mc_score_matrix = np.zeros((len(buildings), N_MC))
mc_rank_matrix = np.zeros((len(buildings), N_MC), dtype=int)
mc_weights = np.random.dirichlet(alpha, size=N_MC)

for i in range(N_MC):
    pv_noisy = np.clip(pv + np.random.normal(0, NOISE_STD, size=len(pv)), 0, 1)
    econ_noisy = np.clip(econ + np.random.normal(0, NOISE_STD, size=len(econ)), 0, 1)
    env_noisy = np.clip(env + np.random.normal(0, NOISE_STD, size=len(env)), 0, 1)
    w1, w2, w3 = mc_weights[i]
    s = w1 * pv_noisy + w2 * econ_noisy + w3 * env_noisy
    mc_score_matrix[:, i] = s
    mc_rank_matrix[:, i] = pd.Series(s).rank(ascending=False, method="min").astype(int).values

buildings["mc_score_mean"] = mc_score_matrix.mean(axis=1)
buildings["mc_score_ci_low"] = np.percentile(mc_score_matrix, 5, axis=1)
buildings["mc_score_ci_high"] = np.percentile(mc_score_matrix, 95, axis=1)
buildings["mc_rank_mean"] = mc_rank_matrix.mean(axis=1)
buildings["mc_rank_std"] = mc_rank_matrix.std(axis=1)
buildings["mc_topN_prob"] = (mc_rank_matrix <= TOP_N).mean(axis=1)

print(f"Monte Carlo ({N_MC} iterasi, noise input ±{NOISE_STD:.0%}, bobot acak):")
print(f"   Rata-rata lebar CI 90% priority_score: "
      f"{(buildings['mc_score_ci_high'] - buildings['mc_score_ci_low']).mean():.3f}")
print(f"   Rata-rata std ranking antar-iterasi   : {buildings['mc_rank_std'].mean():.1f} posisi")
print(f"   Jumlah bangunan Top-{TOP_N} dgn probabilitas >= {STABLE_THRESHOLD:.0%} tetap masuk Top-{TOP_N}: "
      f"{(buildings['mc_topN_prob'] >= STABLE_THRESHOLD).sum()}")

# -----------------------------------------------------
# (b) Bootstrap — resampling bangunan untuk uji stabilitas komposisi Top-N
# -----------------------------------------------------
# Bootstrap dilakukan pada POPULASI bangunan (resample with replacement)
# untuk melihat seberapa stabil KOMPOSISI Top-N bila dataset sedikit
# berbeda (mensimulasikan variasi sampling/pencatatan data bangunan).
N_BOOT = 500
boot_top_ids_count = pd.Series(0, index=buildings["building_id"])
main_top_ids = set(buildings.nsmallest(TOP_N, "rank")["building_id"])

jaccard_scores = []
for b in range(N_BOOT):
    boot_idx = np.random.choice(len(buildings), size=len(buildings), replace=True)
    boot_sample = buildings.iloc[boot_idx]
    boot_score = compute_priority(boot_sample, W_MAIN["w1_pv"], W_MAIN["w2_econ"], W_MAIN["w3_env"])
    boot_rank = boot_score.rank(ascending=False, method="min")
    boot_top_ids = set(boot_sample.loc[boot_rank <= TOP_N, "building_id"])
    boot_top_ids_count.loc[list(boot_top_ids & set(boot_top_ids_count.index))] += 1
    inter = len(boot_top_ids & main_top_ids)
    union = len(boot_top_ids | main_top_ids)
    jaccard_scores.append(inter / union if union > 0 else np.nan)

buildings["bootstrap_topN_freq"] = (boot_top_ids_count / N_BOOT).reindex(buildings["building_id"]).values
mean_jaccard = np.nanmean(jaccard_scores)

print(f"\nBootstrap ({N_BOOT} resample, with replacement):")
print(f"   Rata-rata indeks Jaccard komposisi Top-{TOP_N} (vs data asli): {mean_jaccard:.3f} "
      f"(1.0 = identik sempurna)")
print(f"   Interpretasi: {'komposisi Top-N cukup stabil' if mean_jaccard >= 0.6 else 'komposisi Top-N cukup sensitif terhadap variasi sampel data'}")

# -----------------------------------------------------
# (c) Out-of-sample test — model prediktif (Random Forest, Task 2.2)
# -----------------------------------------------------
# Model 'model' (RandomForestRegressor) dari Task 2.2 bersifat prediktif
# (economic_proxy). Di Task 2 hanya diuji dengan satu train/test split;
