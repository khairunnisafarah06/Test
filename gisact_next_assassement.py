def minmax(s):
    """Min-max scaling yang aman: jika semua nilai sama (min==max),
    kembalikan 0.5 di semua baris (netral) alih-alih NaN/0 yang
    menyesatkan, dan beri peringatan agar penyebabnya jelas terlihat."""
    rng = s.max() - s.min()
    if rng == 0 or pd.isna(rng):
        print(f"   ⚠️ Kolom '{s.name}' punya nilai konstan/kosong (min=max={s.min()}). "
              f"Diisi 0.5 (netral) untuk semua baris.")
        return pd.Series(0.5, index=s.index)
    return (s - s.min()) / rng

