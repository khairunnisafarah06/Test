print("Mengambil data jalan & POI dari OpenStreetMap...")

# --- Tentukan area query OSM dari bounding box bangunan + buffer ---
bounds = buildings.total_bounds  # [minx, miny, maxx, maxy]
center_lat = (bounds[1] + bounds[3]) / 2
center_lon = (bounds[0] + bounds[2]) / 2

# Radius query: dari diagonal bounding box + buffer 1.5 km, dibatasi wajar (max 10 km)
import math
diag_km = math.hypot(bounds[2] - bounds[0], bounds[3] - bounds[1]) * 111  # derajat -> km kasar
query_radius_m = min(max(diag_km * 1000 / 2 + 1500, 1500), 10000)

# -----------------------------------------------------
# 2.1.a — Jaringan jalan & hierarki jalan
# -----------------------------------------------------
try:
    G = ox.graph_from_point((center_lat, center_lon), dist=query_radius_m, network_type="drive")
    edges = ox.graph_to_gdfs(G, nodes=False).to_crs(UTM_CRS)
    edges["highway"] = edges["highway"].astype(str)
    print(f"   ✅ Jaringan jalan: {len(edges)} ruas jalan dimuat.")
except Exception as e:
    print(f"   ⚠️ OSM jalan gagal ({e}), pakai data dummy.")
    edges = gpd.GeoDataFrame(geometry=[], crs=UTM_CRS)

# Hierarki jalan: bobot kepentingan (lebih tinggi = jalan lebih utama)
ROAD_HIERARCHY_WEIGHT = {
    "motorway": 1.0, "trunk": 0.9, "primary": 0.8,
    "secondary": 0.6, "tertiary": 0.4, "residential": 0.2,
    "unclassified": 0.15, "service": 0.1,
}

def road_weight(highway_val):
    """Ambil bobot hierarki tertinggi jika highway berisi list string."""
    if "[" in highway_val:  # contoh: "['primary', 'secondary']"
        types = highway_val.strip("[]").replace("'", "").split(", ")
    else:
        types = [highway_val]
    weights = [ROAD_HIERARCHY_WEIGHT.get(t, 0.1) for t in types]
    return max(weights) if weights else 0.1

if len(edges) > 0:
    edges["road_weight"] = edges["highway"].apply(road_weight)
    main_roads = edges[edges["road_weight"] >= 0.6]   # primary ke atas dianggap "jalan utama"
else:
    main_roads = edges

# -----------------------------------------------------
# 2.1.b — Anchor ekonomi & POI (pusat bisnis, retail, transportasi)
# -----------------------------------------------------
try:
    tags_anchor = {
        "shop": True,
        "amenity": ["marketplace", "bank", "mall", "fuel"],
        "office": True,
        "landuse": ["commercial", "retail"],
    }
    anchors = ox.features_from_point((center_lat, center_lon), tags=tags_anchor, dist=query_radius_m)
    anchors = anchors[anchors.geometry.type.isin(["Point", "Polygon", "MultiPolygon"])].copy()
    anchors["geometry"] = anchors.geometry.centroid
    anchors = anchors.to_crs(UTM_CRS)
    print(f"   ✅ Anchor ekonomi: {len(anchors)} titik dimuat.")
except Exception as e:
    print(f"   ⚠️ OSM anchor ekonomi gagal ({e}), pakai data kosong.")
    anchors = gpd.GeoDataFrame(geometry=[], crs=UTM_CRS)

try:
    tags_transit = {
        "highway": "bus_stop",
        "railway": ["station", "halt"],
        "amenity": "bus_station",
    }
    transit = ox.features_from_point((center_lat, center_lon), tags=tags_transit, dist=query_radius_m)
    transit["geometry"] = transit.geometry.centroid
    transit = transit.to_crs(UTM_CRS)
    print(f"   ✅ Titik transportasi publik: {len(transit)} titik dimuat.")
except Exception as e:
    print(f"   ⚠️ OSM transit gagal ({e}), pakai data kosong.")
    transit = gpd.GeoDataFrame(geometry=[], crs=UTM_CRS)

# -----------------------------------------------------
# 2.1.c — Hitung fitur jarak & kepadatan per bangunan
# -----------------------------------------------------
buildings_m = buildings.to_crs(UTM_CRS).copy()
centroids = buildings_m.geometry.centroid

def min_distance_to(centroids_series, target_gdf, default_val):
    """Jarak minimum dari tiap centroid bangunan ke geometri terdekat di target_gdf."""
    if len(target_gdf) == 0:
        return pd.Series(default_val, index=centroids_series.index)
    target_union = target_gdf.geometry.unary_union
    return centroids_series.distance(target_union)

print("Menghitung fitur jarak & kepadatan...")

buildings["dist_anchor_m"] = min_distance_to(centroids, anchors, default_val=query_radius_m)
buildings["dist_main_road_m"] = min_distance_to(centroids, main_roads, default_val=query_radius_m)
buildings["dist_any_road_m"] = min_distance_to(centroids, edges, default_val=query_radius_m)
buildings["dist_transit_m"] = min_distance_to(centroids, transit, default_val=query_radius_m)

# Hierarki jalan terdekat (ambil road_weight dari ruas jalan terdekat)
if len(edges) > 0:
    nearest_idx = edges.geometry.sindex.nearest(centroids.geometry, return_all=False)
    buildings["road_hierarchy"] = edges.iloc[nearest_idx[1]]["road_weight"].values
else:
    buildings["road_hierarchy"] = 0.1

# Kepadatan bangunan sekitar (buffer 200 m)
buf200 = centroids.buffer(200)
buildings["neighbor_density"] = [
    buildings_m[buildings_m.geometry.intersects(b)].shape[0] - 1
    for b in buf200
]

# Aksesibilitas gabungan (indeks sederhana: makin dekat ke jalan & transit, makin tinggi)
buildings["accessibility_index"] = (
    1 / (1 + buildings["dist_main_road_m"] / 500)
    + 1 / (1 + buildings["dist_transit_m"] / 500)
) / 2

# -----------------------------------------------------
# 2.1.d — Fungsi/okupansi bangunan
# -----------------------------------------------------
if "building" not in buildings.columns:
    buildings["building"] = "yes"

FUNC_SCORE_MAP = {
    "commercial": 1.0, "retail": 0.95, "office": 0.9, "supermarket": 0.95,
    "industrial": 0.7, "mixed": 0.8, "hotel": 0.85,
    "residential": 0.5, "house": 0.45, "apartments": 0.55, "yes": 0.5,
}
buildings["func_score"] = buildings["building"].map(FUNC_SCORE_MAP).fillna(0.5)

