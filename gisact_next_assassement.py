# --- Estimasi area atap potensial (kemiringan & orientasi) ---
# Tanpa data DEM/DSM detail, gunakan asumsi rasio luas atap yang
# benar-benar bisa dipasang panel (mempertimbangkan kemiringan,
# orientasi, dan obstruksi seperti tangga atap/AC/ventilasi).
ROOF_USABLE_RATIO = 0.70   # 70% dari luas footprint

buildings["roof_effective_m2"] = buildings["roof_area_m2"] * ROOF_USABLE_RATIO

# --- Shading sederhana (height & Cloud Fraction) ---
# height_factor : bangunan lebih tinggi -> atap lebih terbuka -> shading factor mendekati 1
# cf_factor     : cloud fraction lebih tinggi -> radiasi efektif berkurang

h_min, h_max = buildings["height"].min(), buildings["height"].max()
if h_max > h_min:
    height_factor = 0.85 + 0.15 * (buildings["height"] - h_min) / (h_max - h_min)
else:
    height_factor = pd.Series(0.90, index=buildings.index)

# CF biasanya 0-1 (fraksi tutupan awan). Faktor reduksi: makin tinggi CF, makin besar reduksi.
cf_factor = 1 - (buildings["CF"].clip(0, 1) * 0.3)   # maksimum reduksi 30% akibat awan

buildings["shading_factor"] = height_factor * cf_factor

# --- Model energi PV (pakai GTI jika tersedia, lebih representatif untuk panel miring) ---
# GTI = Global Tilted Irradiance (kWh/m2/hari) -> lebih akurat dibanding GHI untuk panel ber-tilt
buildings["irradiance_kwh_m2_day"] = buildings["GTI"]
buildings["irradiance_kwh_m2_year"] = buildings["irradiance_kwh_m2_day"] * 365

print("\nPotensi energi surya per bangunan -> selesai dihitung.")

# ==========================================================
# PERHITUNGAN DAYA & ENERGI
# ==========================================================

PANEL_EFFICIENCY  = 0.20    # efisiensi panel 20%
PERFORMANCE_RATIO = 0.80    # performance ratio sistem 80%
PANEL_POWER_WP    = 400     # watt-peak per panel
PANEL_AREA_M2     = 2.0     # luas per panel (m2)

# --- Estimasi kapasitas terpasang (kWp) ---
buildings["n_panels"] = np.floor(buildings["roof_effective_m2"] / PANEL_AREA_M2).astype(int)
buildings["capacity_kwp"] = buildings["n_panels"] * PANEL_POWER_WP / 1000.0

# --- Estimasi produksi energi tahunan (kWh/tahun) ---
# E = Irradiance_tahunan x Luas_atap_efektif x efisiensi_panel x PR x shading_factor
buildings["energy_kwh_yr"] = (
    buildings["irradiance_kwh_m2_year"]
    * buildings["roof_effective_m2"]
    * PANEL_EFFICIENCY
    * PERFORMANCE_RATIO
    * buildings["shading_factor"]
)

print("\n[1.2] Kapasitas & produksi energi -> selesai dihitung.")
print(buildings[["building_id", "roof_area_m2", "capacity_kwp", "energy_kwh_yr"]].describe().round(2))

# ==========================================================
# NORMALISASI INDEKS (MIN-MAX SCALING 0-1) + KLASIFIKASI
# ==========================================================

# Gunakan densitas energi per m2 atap efektif agar bangunan besar & kecil
# bisa dibandingkan secara adil (bukan total energi mentah).
buildings["pv_density"] = buildings["energy_kwh_yr"] / (buildings["roof_effective_m2"] + 1e-9)

mn, mx = buildings["pv_density"].min(), buildings["pv_density"].max()
buildings["pv_index"] = (buildings["pv_density"] - mn) / (mx - mn + 1e-9)

buildings["pv_class"] = pd.cut(
    buildings["pv_index"],
    bins=[-0.001, 0.333, 0.667, 1.001],
    labels=["Rendah", "Sedang", "Tinggi"],
)

print("\nSolar PV Potential Index (0-1) -> selesai.")
print(buildings[["building_id", "pv_index", "pv_class"]].describe(include="all").round(3))

