def minmax(s):
    rng = s.max() - s.min()
    if rng == 0 or pd.isna(rng):
        return pd.Series(0.5, index=s.index)
    return (s - s.min()) / rng


def compute_priority(gdf, w1, w2, w3, label="priority"):
    """
    Weighted Linear Combination (WLC):
    S = w1 * PV + w2 * Economics + w3 * Environment
    dengan w1, w2, w3 >= 0 dan w1+w2+w3 = 1
    """
    assert w1 >= 0 and w2 >= 0 and w3 >= 0, "Bobot tidak boleh negatif."
    total = w1 + w2 + w3
    assert abs(total - 1.0) < 1e-6, f"Bobot harus berjumlah 1, saat ini {total}"

    score = w1 * gdf["pv_index"] + w2 * gdf["econ_index"] + w3 * gdf["env_index"]
    return score

